void AFPSCharacter::SpawDefaultWeapon()
{
	FPSGameMode = FPSGameMode == nullptr ? GetWorld()->GetAuthGameMode<AFPSGameMode>() : FPSGameMode;
	UWorld* World = GetWorld();
	if (FPSGameMode && World && !bElimmed && DefaultWeaponClass)
	{
		AWeapon* StartingWeapon = World->SpawnActor<AWeapon>(DefaultWeaponClass);
		StartingWeapon->bDestroyWeapon = true;
		if (Combat)
		{
			Combat->EquipWeapon(StartingWeapon);
		}
	}
}

void AFPSCharacter::PollInit()
{
	if (FPSPlayerState == nullptr)
	{
		FPSPlayerState = GetPlayerState<AFPSPlayerState>();
		if (FPSPlayerState)
		{
			OnPlayerStateInitialized();

			AFPSGameState* FPSGameState = Cast<AFPSGameState>(UGameplayStatics::GetGameState(this));

			if (FPSGameState && FPSGameState->TopScoringPlayers.Contains(FPSPlayerState))
			{
				MulticastGainedTheLead();
			}
		}
	}
	if (FPSPlayerController == nullptr)
	{
		FPSPlayerController = FPSPlayerController == nullptr ? Cast<AFPSPlayerController>(Controller) : FPSPlayerController;
		if (FPSPlayerController)
		{
			SpawDefaultWeapon();
			UpdateHUDAmmo();
			UpdateHUDHealth();
			UpdateHUDShield();
		}
	}
}

